package com.miempresa.miapp;

public class App {
	
	public static double sumar(int a, int b) {
		return a+b;
	}

	public static double restar(int a, int b) {
		return a-b;
	}
	
	public static double multiplicar(int a, int b) {
		return a*b;
	}

	public static double dividir(int a, int b) {
		return a/b;
	}

}
