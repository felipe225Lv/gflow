package com.miempresa.miapp;

public class Geometria {

	/*
	 * Metodo para calulcar el area de un cuadrado
	 * @param ladoA
	 * @param ladoB
	 * @return ladoA*ladoB
	 */
	public static double areaCuadrardo(double ladoA, double ladoB) {
		return ladoA*ladoB;
	}
	
	public static double areaTriangulo(double base, double altura) {
		return (base*altura)/2;
	}
}
